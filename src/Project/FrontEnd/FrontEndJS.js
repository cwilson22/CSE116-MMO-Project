var socket = io.connect({transports: ['websocket']});
socket.on('gameState', draw);

var players = {"Player1": {"xvalue": 5, "yvalue": 10}, "Player2": {"xvalue": 28, "yvalue": 14},  "Player3": {"xvalue": 14, "yvalue": 4}, "Player4": {"xvalue": 36, "yvalue": 16}};

var c = document.getElementById("myCanvas");
var ctx = c.getContext("2d");
ctx.globalCompositeOperation = 'source-over';

function draw(event) {
    var gs = JSON.parse(event);

    loadGame();

    for(var player in gs){
        if(player["id"] === socket.id) {
            setPlayer(player["x"], player["y"], "#FF4500")
        }else{
            setPlayer(player["x"], player["y"], "#483d8b")
        }
    }
}

function setPlayer(x, y, color) {
    ctx.fillStyle = color;
    ctx.fillRect(x * 32, y * 32, 32, 32);
}

function loadGame(){
    ctx.strokeStyle = '#000000';
    for(var i = 0; i < 40; i++){
        ctx.beginPath();
        ctx.moveTo(i * 32, 0);
        ctx.lineTo(i * 32, 640);
        ctx.stroke();
    }
    for(var j = 0; j < 21; j++){
        ctx.beginPath();
        ctx.moveTo(0, j * 32);
        ctx.lineTo(1248, j * 32);
        ctx.stroke();
    }
}

var keyStates = {
    "w": false,
    "a": false,
    "s": false,
    "d": false
};

function setState(key, toSet){
    if(keyStates[key] !== toSet){
        keyStates[key] = toSet;
        socket.emit("keyStates", JSON.stringify(keyStates));
    }
}

function handleEvent(event, toSet){
    if(event.key === "w" || event.key === "ArrowUp"){
        setState("w", toSet);
    }else if(event.key === "a" || event.key === "ArrowLeft"){
        setState("a", toSet);
    }else if(event.key === "s" || event.key === "ArrowDown"){
        setState("s", toSet);
    }else if(event.key === "d" || event.key === "ArrowRight"){
        setState("d", toSet);
    }
}

document.addEventListener("keydown", function (event) {
    handleEvent(event, true);
});

document.addEventListener("keyup", function (event) {
    handleEvent(event, false);
});